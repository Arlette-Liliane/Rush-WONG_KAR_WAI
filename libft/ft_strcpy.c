#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = ft_strlen(s2);
	while (i <= j)
	{
		s1[i] = s2[i];
		i++;
	}
	return (s1);
}
