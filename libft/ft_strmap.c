#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	char	*s1;

	i = 0;
	s1 = ft_strnew(ft_strlen(s));
	while (s[i] != '\0')
	{
		s1[i] = f(s[i]);
		i++;
	}
	return (s1);
}
